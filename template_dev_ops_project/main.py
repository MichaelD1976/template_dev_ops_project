def add(a, b):
    """Return the sum of a and b."""
    return a + b


def multiply(a, b):
    """Return the product of a and b."""
    return a * b

def subtract(a, b):
    """Return the difference between a and b."""
    return a - b

def division(a, b):
    """Return the product of a and b."""
    return a / b

